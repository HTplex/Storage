package practise;
import java.util.Scanner;

public class Twostamplenine {
	public static void main (String[]args){
		Scanner input=new Scanner(System.in);
		int x=input.nextInt();
		System.out.println(x);
		x++;
		System.out.println(x);
		++x;
		System.out.println(x);
		--x;
		System.out.println(x);
		x--;
		System.out.println(x);
		x+=3;
		System.out.println(x);
		x%=3;
		System.out.println(x);
		
	}
}
